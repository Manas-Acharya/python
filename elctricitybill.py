u=float(input("Enter number of units : "))

if u<=100:
    print("Electricity bill : ",u*2)

if u>100 and u<=200:
    print("Electricity bill : ",u*3)

if u>200 and u<=300:
    print("Electricity bill : ",u*4)

if u>300 and u<=400:
    print("Electricity bill : ",u*5)

if u>400 and u<=500:
    print("Electricity bill : ",u*6)

if u>500:
    print("Electricity bill : ",u*7)
